#include <stdio.h>
#include <stdlib.h>
#include <locale.h>
#include <string.h>

int main(){
	
	setlocale(LC_ALL, "Portuguese_Brazil");	
	char nome[80], sobrenome[80];
	
	printf("Digite o seu nome: ");
	gets(nome); /* Imprime uma string de cada vez */
	printf("Digite o seu sobrenome: ");
	gets(sobrenome); /* Imprime uma string de cada vez */
	strcat(nome, sobrenome);
	
	printf("Após concatenar as strings, o nome é: %s\n", nome);
	if (strstr(sobrenome, nome) == 0){
		printf("Agora o sobrenome está contido na variável nome. \n");
	}
	strlwr(nome);
	printf("O nome em minúsculo é: %s\n", nome);
	strupr(nome);
	printf("O nome em maiúsculo é: %s\n", nome);
	
	return (0);
}
